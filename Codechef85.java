package Codechef;

public class Codechef85 {
	public static void main(String[] args) {
        // Initialize a string variable
        String greeting = "Hello, World!";
        
        // Check if the string starts with "Hello"
        boolean startsWithHello = greeting.startsWith("Hello"); // Returns true

        // Check if the string starts with "hi"
        boolean startsWithhi = greeting.startsWith("hi"); // Returns false

        // Print the results
        System.out.println("Starts with 'Hello': " + startsWithHello); 
        System.out.println("Starts with 'hi': " + startsWithhi);  
    }
}
