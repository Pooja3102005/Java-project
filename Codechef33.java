package Codechef;

public class Codechef33 {
	public static void main(String[] args) {
        int total = 10;  // Initial value
        total += 5;      // Adds 5 to total (total = total + 5)
        System.out.println("Updated Total: " + total);
        
        total =+ 100; // It is same as total = +100 or total = 100
        System.out.println("Again updated Total: " + total);

    }
}
