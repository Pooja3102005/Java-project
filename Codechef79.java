package Codechef;

public class Codechef79 {
	public static void main(String[] args) {
        // Define a string
        String sentence = "Learning Java is fun!";

        // Find the index of the first occurrence of the character 'J'
        int indexOfJ = sentence.indexOf('J');

        // Print the index
        System.out.println("The index of 'J' is: " + indexOfJ);

        // Find the index of the first occurrence of the character 'a'
        int indexOfA = sentence.indexOf('a');

        // Print the index
        System.out.println("The index of 'a' is: " + indexOfA);
    }
}
