package Codechef;
import java.util.*;

//Class definition
public class Notequal {

 public static void main(String[] args) {
     // Declare an integer variable 'number' and assign it a value
     int number = 15;

     // Check if 'number' is not equal to 10
     if (number != 10) {
         // If the condition is true, print this message
         System.out.println("The number is not 10.");
     } else {
         // If the condition is false (number is 10), print this message
         System.out.println("The number is exactly 10.");
     }

     // This statement executes regardless of the condition
     System.out.println("Comparison check completed.");
 }
}
