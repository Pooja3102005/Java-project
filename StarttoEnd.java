package Codechef;

public class StarttoEnd {
	public static void main(String[] args) {
        int start = 2; // Starting value of the range
        int end = 5; // Ending value of the range

        // Loop from start to end, printing each number
        for (int i = start; i <= end; i++) {
            System.out.println(i); // Print the current value of i
        }
    }
}
