package Codechef;

public class Codechef40 {
	public static void main(String[] args) {
        // Declare an integer variable 'count' and initialize it with 5
        int count = 5;

        // Print the initial value of 'count'
        System.out.println("Before increment: " + count);

        // Post-increment operation: prints the current value of 'count' and then increments it
        System.out.println("Using count++: " + count++);

        // Print the value of 'count' after the post-increment
        System.out.println("After increment: " + count);
    }
}
