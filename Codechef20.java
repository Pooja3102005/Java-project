public class Codechef20 {
	public static void main(String[] args) {
        double price = 99.99; // Double value
        int discountedPrice = (int) price; // Explicit conversion from double to int

        System.out.println("Original Price: " + price);
        System.out.println("Discounted Price (after conversion): " + discountedPrice);
    }
}
