package Codechef;

public class Codechef35 {
	 public static void main(String[] args) {
	        int balance = 100;  // Initial value
	        balance -= 30;      // Subtract 30 from balance (balance = balance - 30)

	        System.out.println("Remaining Balance: " + balance);
	        
	        balance =- 1000;   // It is same as balance = -1000
	        System.out.println("Again updated balance: " + balance);
	    }
}
